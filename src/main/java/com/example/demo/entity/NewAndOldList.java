package com.example.demo.entity;

import java.util.List;

public class NewAndOldList {
	private List<Integer> newList;
	private List<Integer> oldList;
	public List<Integer> getNewList() {
		return newList;
	}
	public void setNewList(List<Integer> newList) {
		this.newList = newList;
	}
	public List<Integer> getOldList() {
		return oldList;
	}
	public void setOldList(List<Integer> oldList) {
		this.oldList = oldList;
	}
	
}
